    private void tbEstudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEstudiantesMouseClicked
        int seleccion = tbEstudiantes.rowAtPoint(evt.getPoint());
        estudianteSeleccionado.setIdEstudiante(Integer.parseInt(String.valueOf(tbEstudiantes.getValueAt(seleccion, 0))));
        estudianteSeleccionado.setPrimerNombre(String.valueOf(tbEstudiantes.getValueAt(seleccion, 1)));
        estudianteSeleccionado.setSegundoNombre(String.valueOf(tbEstudiantes.getValueAt(seleccion, 2)));
        estudianteSeleccionado.setPrimerApellido(String.valueOf(tbEstudiantes.getValueAt(seleccion, 3)));
        estudianteSeleccionado.setSegundoApellido(String.valueOf(tbEstudiantes.getValueAt(seleccion, 4)));
        estudianteSeleccionado.setActivo(Boolean.parseBoolean(String.valueOf(tbEstudiantes.getValueAt(seleccion, 5))));
        llenarCampos();
    }//GEN-LAST:event_tbEstudiantesMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Boolean resultado = EstudianteDAO.eliminarEstudiante(estudianteSeleccionado.getIdEstudiante());
        if(resultado){
            JOptionPane.showMessageDialog(null, "Eliminacion exitosa");
        }else{
            JOptionPane.showMessageDialog(null, "Fallo al eliminar al estudiante");
        }
        limpiarCampos();
        cargarDatos();
    }//GEN-LAST:event_btnEliminarActionPerformed

