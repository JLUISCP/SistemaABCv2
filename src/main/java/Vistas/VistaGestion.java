    private void cargarDatos() {
        DefaultTableModel model = new DefaultTableModel();
        ResultSet rs = EstudianteDAO.consultarEstudiantes();
        model.setColumnIdentifiers(new Object[]{"idEstudiante","Primer nombre", "Segundo nombre", "Primer apellido", "Segundo apellido", "Estado actividad"});
        try{
            while(rs.next()){
                model.addRow(new Object[]{rs.getInt("idestudiante"), rs.getString("primernombre"), rs.getString("segundonombre"), rs.getString("primerapellido"), rs.getString("segundoapellido"), rs.getBoolean("activo")});
                System.out.println("hlou");
                
            }
            tbEstudiantes.setModel(model);
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        btnEliminar.setEnabled(false);
        btnModificar.setEnabled(false);
    }

    private void llenarCampos() {
        tfPrimerNombre.setText(estudianteSeleccionado.getPrimerNombre());
        tfSegundoNombre.setText(estudianteSeleccionado.getSegundoNombre());
        tfPrimerApellido.setText(estudianteSeleccionado.getPrimerApellido());
        tfSegundoApellido.setText(estudianteSeleccionado.getSegundoApellido());
        chActivo.setSelected(estudianteSeleccionado.getActivo());
        btnEliminar.setEnabled(true);
        btnModificar.setEnabled(true);
        btnLimpiar.setEnabled(true);
        btnRegistrar.setEnabled(false);
    }

    private void limpiarCampos() {
        tfPrimerNombre.setText("");
        tfSegundoNombre.setText("");
        tfPrimerApellido.setText("");
        tfSegundoApellido.setText("");
        chActivo.setSelected(false);
        btnLimpiar.setEnabled(false);
        btnRegistrar.setEnabled(true);
        btnEliminar.setEnabled(false);
        btnModificar.setEnabled(false);
    }

    private Boolean validarCamposVacios() {
        Boolean camposLLenos = true;
        if(tfPrimerNombre.getText() == ""){
            camposLLenos = false;
        }
        if(tfSegundoNombre.getText() == ""){
            camposLLenos = false;
        }
        if(tfPrimerApellido.getText() == ""){
            camposLLenos = false;
        }
        if(tfSegundoApellido.getText() == ""){
            camposLLenos = false;
        }
        return camposLLenos;
    }
